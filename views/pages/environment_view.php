					<h4><a name="planahead" id="planahead"></a>Plan Ahead and Prepare</h4>
					<ul type="disc">
						<li>Know the regulations and special concerns for the area you'll visit.</li>
						<li>Prepare for extreme weather, hazards, and emergencies.</li>
						<li>Schedule your trip to avoid times of high use.</li>
						<li>Visit in small groups. Split larger parties into groups of 4-6.</li>
						<li>Repackage food to minimize waste.</li>
						<li>Use a map and compass to eliminate the use of marking paint, rock cairns or flagging.</li>
						<p><a class="go-top" href="<?= site_url() ?>menu_pages/environment#go-top">Go top</a></p>
					</ul>
					<h4><a name="travelandcamp" id="travelandcamp"></a>Travel and Camp on Durable Surfaces</h4>
					<ul type="disc">
						<li>Durable surfaces include established trails and campsites, rock, gravel, dry grasses or
							snow.
						</li>
						<li>Protect riparian areas by camping at least 200 feet from lakes and streams.</li>
						<li> Good campsites are found, not made. Altering a site is not necessary.</li>
						<li><br/>
						</li>
						<p>In popular areas:</p>
						<ul type="circle">
							<li>Concentrate use on existing trails and campsites.</li>
							<li>Walk single file in the middle of the trail, even when wet or muddy.</li>
							<li>Keep campsites small. Focus activity in areas where vegetation is absent.</li>
						</ul>
						<p>In pristine areas:</p>
						<ul type="circle">
							<li>Disperse use to prevent the creation of campsites and trails.</li>
							<li>Avoid places where impacts are just beginning.</li>
						</ul>
						<p><a class="go-top" href="<?= site_url() ?>menu_pages/environment#go-top">Go top</a></p>
					</ul>
					<h4><a name="disposeofwaste" id="disposeofwaste"></a>Dispose of Waste Properly</h4>
					<ul type="disc">
						<li>Pack it in, pack it out. Inspect your campsite and rest areas for trash or spilled foods.
							Pack out all trash, leftover food, and litter.
						</li>
						<li>Deposit solid human waste in catholes dug 6 to 8 inches deep at least 200 feet from water,
							camp, and trails. Cover and disguise the cathole when finished.
						</li>
						<li>Pack out toilet paper and hygiene products.</li>
						<li>To wash yourself or your dishes, carry water 200 feet away from streams or lakes and use
							small amounts of biodegradable soap. Scatter strained dishwater.
						</li>
						<p><a class="go-top" href="<?= site_url() ?>menu_pages/environment#go-top">Go top</a></p>
					</ul>
					<h4><a name="leavewhatyoufind" id="leavewhatyoufind"></a>Leave What You Find</h4>
					<ul type="disc">
						<li>Preserve the past: examine, but do not touch, cultural or historic structures and
							artifacts.
						</li>
						<li>Leave rocks, plants and other natural objects as you find them.</li>
						<li>Avoid introducing or transporting non-native species.</li>
						<li>Do not build structures, furniture, or dig trenches.</li>
					</ul>
					<h4><a name="minimizecampfire" id="minimizecampfire"></a>Minimize Campfire Impacts</h4>
					<ul type="disc">
						<li>Campfires can cause lasting impacts to the backcountry. Use a lightweight stove for cooking
							and enjoy a candle lantern for light.
						</li>
						<li>Where fires are permitted, use established fire rings, fire pans, or mound fires.</li>
						<li>Keep fires small. Only use sticks from the ground that can be broken by hand.</li>
						<li>Burn all wood and coals to ash, put out campfires completely, then scatter cool ashes.</li>
						<p><a class="go-top" href="<?= site_url() ?>menu_pages/environment#go-top">Go top</a></p>
					</ul>
					<h4><a name="respectwildlife" id="respectwildlife"></a>Respect Wildlife</h4>
					<ul type="disc">
						<li>Observe wildlife from a distance. Do not follow or approach them.</li>
						<li>Never feed animals. Feeding wildlife damages their health, alters natural behaviors, and
							exposes them to predators and other dangers.
						</li>
						<li>Protect wildlife and your food by storing rations and trash securely.</li>
						<li>Control pets at all times, or leave them at home.</li>
						<li>Avoid wildlife during sensitive times: mating, nesting, raising young, or winter.</li>
						<p><a class="go-top" href="<?= site_url() ?>menu_pages/environment#go-top">Go top</a></p>
					</ul>
					<h4><a name="beconsiderate" id="beconsiderate"></a>Be Considerate of Other Visitors</h4>
					<ul type="disc">
						<li>Respect other visitors and protect the quality of their experience.</li>
						<li>Be courteous. Yield to other users on the trail.</li>
						<li>Step to the downhill side of the trail when encountering pack stock.</li>
						<li>Take breaks and camp away from trails and other visitors.</li>
						<li>Let nature's sounds prevail. Avoid loud voices and noises.</li>
